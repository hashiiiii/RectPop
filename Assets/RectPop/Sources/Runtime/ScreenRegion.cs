namespace RectPop
{
    /// <summary> defines the screen region for the UI element relative to the target RectTransform. </summary>
    /// <remarks> screen is divided into 9 regions similar to a 9-slice layout. </remarks>
    public enum ScreenRegion
    {
        TopLeft,
        TopCenter,
        TopRight,
        MiddleLeft,
        MiddleCenter,
        MiddleRight,
        BottomLeft,
        BottomCenter,
        BottomRight,
    }
}